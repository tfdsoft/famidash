void state_menu() {
    unsigned char menu_color = 0x11;
    unsigned short interrupt_scroll = 0;
    unsigned short scroll_bank = 0;
    signed char selection = 0;

    automatic_fs_updates=1;

    // load parallax background
    vram_adr(0x1000);
    set_chr_mode_0(0x10);
    set_chr_mode_1(0x12);
    
    if(loaded_bg_set != background_set){
        donut_decompress_vram(chr_bg[background_set], chr_bank_2);
        vram_generate_parallax(background_set);
        loaded_bg_set = background_set;
    }

    set_chr_mode_2(4);
    //set_chr_bank(3,5);
    set_chr_mode_4(6);
    set_chr_mode_5(7);

    // load menu stuff
    vram_adr(0x000);
    donut_decompress_vram(chr_menu_global, chr_bank_0);
    donut_decompress_vram(chr_font, chr_bank_0);
    donut_decompress_vram(chr_menu_famidash, chr_bank_0);
    donut_decompress_vram(chr_menu_buttons, chr_bank_0);

    // load ground
    set_chr_mode_0(0x8);
    donut_decompress_vram(chr_g[ground_set], chr_bank_3);
    // the "robtop/tfdsoft" text is there
    donut_decompress_vram(chr_menu_robtop, chr_bank_0);

    // set second sprite bank to the big buttons
    set_chr_mode_1(0x6);
    
    // set second bg bank to parallax
    set_chr_mode_3(0x10);

    // write the parallax tilemap
    vram_adr(0x2000);
    vram_write_parallax(background_set);

    // write the title screen tilemap
    vram_adr(0x2000);
    vram_unrle_ignore0(nt_title);
    vram_unrle(nt_title);

    

    pal_all(pal_title);

    
    flush_irq();
    add_advanced_interrupt( // set button chr
        94, 
        irq_set_chr,
        args88(5,7)
    );
    add_advanced_interrupt( // ground scroll
        79, 
        irq_set_chr_and_scroll,
        args88(5,8)
    );
    add_advanced_interrupt( // set background chr
        47, 
        irq_set_chr_and_scroll,
        args88(3,0x10)
    );
    enable_irq();
    
    // run once before fading in
    oam_clear();
    oam_meta_spr(1,0,mspr_title);

    str_vram_buffer(str_version, NT_ADR_A(20,9));

    #ifdef FLAG_ISDEMO
        str_vram_buffer(str_demo, NT_ADR_A(1,3));
        one_vram_buffer(FLAG_ISDEMO, NT_ADR_A(6,3));
    #endif

    ppu_on_all();
    pal_fade_to(1,4);

    // for some reason, the compiler
    // absolutely REFUSES to put an
    // lda #0 before running this, so
    // do it manually here (if song is 0)
    __asm__("lda #0"); 
    if(!famistudio_song_speed) {
        music_play(saved_menu_theme);
    }

    
    //interrupt_scroll = 0;
    while(1){
        ppu_wait_nmi();
        scroll(0,0);
        oam_clear();

        interrupt_scroll += phys_speed[1];
        IRQ(1).arg2 = high_byte(interrupt_scroll);

        scroll_bank += (phys_speed[1] >> 3);
        if(high_byte(scroll_bank) >= loaded_bg_width) high_byte(scroll_bank) = 0;
        IRQ(2).arg1 = (high_byte(scroll_bank)+0x10);
        


        if((FRAME_CNT & 0x3f) < 5) {

            if(FRAME_CNT & 0x1) menu_color -= 1;
            else menu_color += 1;

            if(menu_color >= 0x1d) menu_color = 0x11;
            if(menu_color == 0x10) menu_color = 0x1c;

            pal_col(0, menu_color);
            pal_col(2, menu_color-0x10);
            pal_col(9, menu_color-0x10);
            pal_col(10, menu_color);
        }



        
        
        if((selection >= 2) && (selection < 5)){
            oam_meta_spr(
                low_byte(mspr_select_pos[selection]),
                high_byte(mspr_select_pos[selection]),
                mspr_selectbox
            );
        } else {
            oam_meta_spr(
                low_byte(mspr_select_pos[selection]),
                high_byte(mspr_select_pos[selection]),
                mspr_selectarrow
            );
        }

        // the fact that i can use a metasprite for
        // EVERYTHING on the menu is bloody brilliant
        oam_meta_spr(1,0,mspr_title); // one to the right
                                    // so i can put sprites at
                                    // x = 128
        
        if(player1_pressed & PAD_RIGHT) selection++;
        if(player1_pressed & PAD_LEFT) selection--;

        if(player1_pressed & PAD_DOWN) selection += (menu_button_count>>1);
        if(player1_pressed & PAD_UP) selection -= (menu_button_count>>1);

        if(selection >= ((uint8_t)menu_button_count)) {
            selection -= menu_button_count;
        }
        if(selection < 0) {
            selection += menu_button_count;
        }

        if(player1_pressed & PAD_B) {
            break;
        }

        if(player1_pressed & PAD_A) {
            switch(selection){
                case 0: // play
                    gamestate = 0x11;
                    break;
                /*case 1: // community
                    break;
                case 2: // settings
                    break;*/
                case 3: // music
                    gamestate = 0x14;
                    break;
                /*case 4: // help
                    break;
                case 5: // icon kit
                    break;*/
                default:
                    sfx_play(sfx_gold01,0);
            }
            pal_fade_to(4,0);
            break;
        }
        //if(player1_pressed & PAD_SELECT) {
        //    gamestate = 0xf0;
        //    break;
        //}

    }
}





