	mmc3_disable_irq();
	
	ppu_off();
	pal_bright(0);
    
	pal_bg(palette_Credits2);
	
	vram_adr(NAMETABLE_A);
	vram_unrle(playmain);

	// __asm__("LDA mmc3PRG1Bank \nPHA ");
